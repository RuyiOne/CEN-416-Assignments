START:	MOV AL, 80		
	OUT 06, AL		;SET CONTROL BYTE FOR PROGRAMMABLE PERIPHERAL INTERFACE

	;3F IS USED FOR DISPLAYING ZERO IN SEVEN SEGMENT DISPLAY
	;06 IS USED FOR DISPLAYING ONE IN SEVEN SEGMENT DISPLAY
	;5B IS USED FOR DISPLAYING TWO IN SEVEN SEGMENT DISPLAY
	;4F IS USED FOR DISPLAYING THREE IN SEVEN SEGMENT DISPLAY
	;66 IS USED FOR DISPLAYING FOUR IN SEVEN SEGMENT DISPLAY
	;6D IS USED FOR DISPLAYING FIVE IN SEVEN SEGMENT DISPLAY
	;7D IS USED FOR DISPLAYING SIX IN SEVEN SEGMENT DISPLAY
	;47 IS USED FOR DISPLAYING SEVEN IN SEVEN SEGMENT DISPLAY
	;7F IS USED FOR DISPLAYING EIGHT IN SEVEN SEGMENT DISPLAY
	;6F IS USED FOR DISPLAYING NINE IN SEVEN SEGMENT DISPLAY
	
	MOV [210], 06		;THE MEMORY REPRESENTED BY THE DISPLACEMENT VALUE 210 WILL BE USED FOR SEVEN SEGMENT DISPLAY ONE
	MOV [211], 3F		;THE MEMORY REPRESENTED BY THE DISPLACEMENT VALUE 211 WILL BE USED FOR SEVEN SEGMENT DISPLAY TWO
	MOV [212], 06		;THE MEMORY REPRESENTED BY THE DISPLACEMENT VALUE 212 WILL BE USED FOR SEVEN SEGMENT DISPLAY THREE
	MOV [213], 3F		;THE MEMORY REPRESENTED BY THE DISPLACEMENT VALUE 213 WILL BE USED FOR SEVEN SEGMENT DISPLAY FOUR
	MOV [218], 1F		;BY DEFAULT JANUARY IS SET TO 31 DAYS (1F)
	MOV [215], 1		;BY DEFAULT START DATE IS 1ST JANUARY (1)
	MOV [216], 1		;BY DEFAULT START DATE IS 1ST JANUARY (1)

;THIS SEGMENT GENERATES TWO SECONDS BY LOOPING THE COUNTER AND PROCEEDS AFTER TWO SECONDS
DELAY:	MOV CX, 1		; SET COUNTER REGISTER TO ONE
NEXT:	LOOP NEXT		; LOOP COUNTER
	INC DX			; INCREMENT DATA REGISTER
	CALL DISPVAL		;CALL SUBROUTINE THAT DISPLAYS THE SEVEN SEGMENT DISPLAY AT INTERVALS
	CMP DX,XXXX		;CMP TO SEE IF VALUE IN DATA REGISTER MATCHES THE GIVEN VALUE
	JNE DELAY		;JUMP IF NOT EQUAL TO DELAY

;THIS SECTION GENERATES ONE DAY AND PROCEEDS AFTER ONE DAY OR 24 HOURS ELAPSES
	MOV DX, 0000
	INC BX
	CMP BX, A8C0		;AC80 IS 43200 IN DECIMAL, 2 SECONDS TIMES 43200 = 86400 SECONDS
	JNE DELAY
	MOV BX, 0000		;CLEAR BASE REGISTER BEFORE NEW COUNT IS TAKEN

